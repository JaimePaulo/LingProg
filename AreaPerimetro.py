# Problema - Calcular a area e o perimetro de um 
#           quadrado ou retangulo

# Ler o tamanho do lado do quadrado/Retangulo
lado = int(input("Qual o tamanho do lado em m?"))

# Calcular a area do quadrado
area = lado * lado

# Calcular o Perimetro do Quadrado
perimetro = 4 * lado

# Apresentar o resultado
print("Area =", area)
print("Perimetro =", perimetro)
